package com.home_27_02_23;

import java.util.Random;   /// импортировать класс Random из пакета java.util

public class Task_8_18 { /// Запишіть метод, який перемішує рядки у двовимірному int масиві за допомогою наступного заголовка: статична порожня довільна дія (int[][] m)

    public static void shuffle(int[][] m) { //  объявить метод shuffle, который принимает двумерный массив целых чисел m в качестве аргумента.
        Random rand = new Random(); // создать объект класса Random с именем rand
        for (int i = m.length - 1; i > 0; i--) {  //  начать цикл, который итерирует по всем строкам массива m в обратном порядке, начиная с последней строки
            int[] temp = m[i]; /// сохранить текущую строку в переменной temp
            int j = rand.nextInt(i+1); // сгенерировать случайное число j от 0 до i, где i - индекс текущей строки.
            m[i]=m[j]; // заменить текущую строку на строку с индексом j
            m[j]=temp; // заменить строку с индексом j на сохраненную строку temp.
        }
        }
    }
/// Метод shuffle принимает в качестве входных данных двумерный массив целых чисел m и перемешивает его строки на месте. Метод использует алгоритм перетасовки Фишера-Йейтса, который работает путем перебора строк массива в обратном порядке и для каждой строки заменяет ее случайно выбранной строкой из оставшихся строк.
//
//Класс Random используется для генерации случайного целого числа j от 0 до i, где i — индекс текущей обрабатываемой строки. Переменная temp используется для хранения ссылки на заменяемую строку, и две строки меняются местами с использованием стандартного алгоритма замены.
//
//Обратите внимание, что метод shuffle изменяет входной массив на месте, поэтому, если вы хотите сохранить исходный массив, вы должны сделать его копию перед вызовом метода. Кроме того, метод перемешивания предполагает, что входной массив не равен нулю и содержит хотя бы одну строку.