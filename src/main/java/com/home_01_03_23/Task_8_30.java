package com.home_01_03_23;

public class Task_8_30 {

    public static double[] linearEquation(double[][] a, double[] b) {
        double det = a[0][0] * a[1][1] - a[0][1] * a[1][0];
        if (det == 0) {
            System.out.println("The equation has no solution.");
            return null;
        } else {
            double[] result = new double[2];
            result[0] = (b[0] * a[1][1] - b[1] * a[0][1]) / det;
            result[1] = (b[1] * a[0][0] - b[0] * a[1][0]) / det;
            return result;
        }
    }
}

///Метод принимает двумерный массив a, представляющий коэффициенты линейных уравнений, и одномерный массив b, представляющий константы. Он возвращает одномерный массив, содержащий решения x и y.
//
//Метод сначала вычисляет определитель матрицы a по формуле det = a00a11 - a01a10. Если определитель равен 0, он печатает сообщение об ошибке и возвращает ноль, указывая на то, что уравнения не имеют решения. В противном случае он вычисляет x и y по формулам, приведенным в условии задачи, и возвращает их в виде массива.

